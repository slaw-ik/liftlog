const i18n = new I18n(translations);

// Set default locale
i18n.defaultLocale = 'en';
i18n.enableFallback = true;

// Safely get device locale
try {
  const deviceLocale = Localization.locale || Localization.getLocales()?.[0]?.languageCode || 'en';
  i18n.locale = deviceLocale.split('-')[0]; // Get language code (e.g., 'en' from 'en-US')
} catch (error) {
  i18n.locale = 'en'; // Fallback to English
}

// Load saved language preference
export const initializeI18n = async () => {
  try {
    const savedLanguage = await AsyncStorage.getItem('appLanguage');
    if (savedLanguage) {
      i18n.locale = savedLanguage;
    } else {
      // Try to detect device language on the first launch
      try {
        const locales = Localization.getLocales();
        if (locales && locales.length > 0) {
          const deviceLang = locales[0].languageCode || 'en';
          // Only set if we support this language
          if (['en', 'ru', 'uk'].includes(deviceLang)) {
            i18n.locale = deviceLang;
          }
        }
      } catch (localeError) {
        console.log('Could not detect device language, using default');
      }
    }
  } catch (error) {
    console.error('Error loading language preference:', error);
  }
};

// Save language preference
export const setLanguage = async (language: string) => {
  try {
    i18n.locale = language;
    await AsyncStorage.setItem('appLanguage', language);
  } catch (error) {
    console.error('Error saving language preference:', error);
  }
};

